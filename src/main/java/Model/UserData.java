public class UserData {
    private String fullName;
    private String userName;
    private String phoneNumber;
    private String cmtUser;
    private String birthday;
    private String maSoNhanVien;


    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getCmtUser() {
        return cmtUser;
    }

    public void setCmtUser(String cmtUser) {
        this.cmtUser = cmtUser;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getMaSoNhanVien() {
        return maSoNhanVien;
    }

    public void setMaSoNhanVien(String maSoNhanVien) {
        this.maSoNhanVien = maSoNhanVien;
    }
}
